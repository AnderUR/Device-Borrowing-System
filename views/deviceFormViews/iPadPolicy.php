<p>
<h5 class="policySecTitle">Eligibility</h5>
<div class="policyContent">
    <br />
    iPad Minis can be loaned to currently enrolled students who maintain a valid library account in good standing and who read, agree to, and sign the iPad Mini Loan Agreement.
    <br />
    Items loaned must be returned, including:
    <br />
    <ul>
        <li>1 iPad Mini  </li>
        <li>1 Thunderbolt cable adapter</li>
        <li>1 Power adapter</li>
        <li>1 iPad Mini case</li>
        <li>1 softcase</li>
    </ul>
    <br />
    The Loan Agreement must be read and signed each time an iPad is borrowed. Only one iPad Mini can be checked out per person.
    <br />
    <br />
</div>
<h5 class="policySecTitle">Loan period</h5>
<div class="policyContent">
    <br />
    <ul>
        <li>iPad Minis are loaned for 3 days.</li>
        <li><b>No renewals are permitted.</b></li>
        <li>The iPad Minis will be loaned on a first-come, first served basis. You may not reserve an iPad Mini.</li>
        <li>There is a 1-day grace period for iPad returns.</li>
    </ul>
    <br />
</div>
<h5 class="policySecTitle">Returns</h5>
<div class="policyContent">
    <br />
    iPad Minis must be returned in good working order to the library’s Device Borrowing System 30 minutes before closing.

    Upon returning, allow a minimum of 30 minutes for inspection.

    iPad Minis must be returned Monday – Friday NO LATER THAN:
    <br />
    6:30pm Monday – Thursday
    5:30pm Friday
    <br />
    The Device Borrowing System is closed on weekends.
    <br />
    The working condition of the iPad will be inspected by a library staff member before checkout. Upon its return, a staff member will verify that the iPad is in working order before the borrower leaves the desk. It is the borrower’s responsibility to allot 30 minutes for this.
    <br /><br />
</div>
<h5 class="policySecTitle">Fines and Damages</h5>
<div class="policyContent">
    <br />
    Borrowers are required to immediately report any problems to the library’s Device Borrowing System experienced with the iPad during their borrowing period.
    <br />
    <br />
    <ul>
        <li>Late fines will be charged at the rate of $100 per day to a maximum of $400.</li>
        <li>If an iPad Mini is more than 4 days overdue, it is considered lost.</li>
        <li>Borrowers are financially responsible for the iPad Mini loan and must pay all repairs and/or replacement costs resulting from damage or loss. Costs are dependent on the extent of the damage of the iPad. If the iPad is beyond repair, borrowers must pay for the replacement cost of all materials.</li>
        <li>The replacement cost of an iPad Mini is $400 (iPad and App Purchases).</li>
        <li>The cost of replacing the power cable and USB adapter is $50.</li>
        <li>The cost of the softcase is $17.00.</li>
        <li>The processing fee for lost iPads is $100.</li>
        <li>A lost and overdue iPad may result in a maximum of $950 (overdue fine + replacement cost + case fee + USB cable + power adaptor + processing fee).</li>
        <li>Do not leave the iPad Mini unattended.</li>
        <li>In case of an emergency evacuation, take the iPad Mini with you.</li>
        <li>Noncompliance with the above rules will result in a registration block and suspension of library privileges.</li>
    </ul>
    <br />
</div>
<h5 class="policySecTitle">Personal Data and Information</h5>
<div class="policyContent">
    <br />
    The iPad Minis come with a suite of preloaded applications. Borrowers may add additional data and applications, however all data from the iPad Mini will be wiped and replaced with the standard set of preloaded apps once it is returned to the library.
    <br />
    Any additional apps or media purchased by the borrower during the loan period must be done with the borrower’s own funds and iTunes ID. Any data or documents saved to the iPad by the patron during the loan period will be permanently erased during the restore process.
</div>

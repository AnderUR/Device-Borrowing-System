<p>
<h5 class="policySecTitle">Eligibility</h5>
<div class="policyContent">
    <br />
    Laptops can be loaned to currently enrolled students who maintain a valid library account in good standing and who read, agree to, and sign the Laptop Loan Agreement.
    <br />
    Items loaned must be returned, including:
    <br />
    <ul>
        <li>1 Lenovo 100e Laptop</li>
        <li>1 charger</li>
        <li>1 soft case</li>
    </ul>
    <br />
    The Loan Agreement must be read and signed each time a laptop is borrowed. Only one laptop can be checked out per person.    <br />
    <br />
</div>
<h5 class="policySecTitle">Loan period</h5>
<div class="policyContent">
    <br />
    <ul>
        <li>Laptop are loaned for 7 days.</li>
        <li><b>No renewals are permitted.</b></li>
        <li>The laptop will be loaned on a first-come, first served basis. You may not reserve a laptop.</li>
    </ul>
    <br />
</div>
<h5 class="policySecTitle">Returns</h5>
<div class="policyContent">
    <br />
    iPad Minis must be returned in good working order to the library’s Device Borrowing System 30 minutes before closing.

    Upon returning, allow a minimum of 30 minutes for inspection.

    The working condition of the laptop will be inspected by a library staff member before checkout. Upon its return, a staff member will verify that the laptop is in working order before the borrower leaves the desk. It is the borrower’s responsibility to allot 30 minutes for this.
    <br /><br />
</div>
<h5 class="policySecTitle">Fines and Damages</h5>
<div class="policyContent">
    <br />
    Borrowers are required to immediately report any problems to the library’s Device Borrowing System experienced with the iPad during their borrowing period.
    <br />
    <br />
    <ul>
        <li>Late fines will be charged at the rate of $100 per day to a maximum of $400.</li>
        <li>If a laptop is more than 4 days overdue, it is considered lost.</li>
        <li>Borrowers are financially responsible for the laptop loan and must pay all repairs and/or replacement costs resulting from damage or loss. Costs are dependent on the extent of the damage of the laptop. If the laptop is beyond repair, borrowers must pay for the replacement cost of all materials.</li>
        <li>Repairs include damage from software such as bad applications, malware, and viruses. </li>
        <li>The replacement cost of a laptop is $325 (laptop, charger, and case).</li>
        <li>The cost of replacing a laptop is $200.</li>
        <li>The cost of replacing the case, and power adapter is $100.</li>
        <li>The processing fee for lost laptops is $25.</li>
        <li>A lost and overdue laptop may result in a maximum of $700(overdue fine + replacement cost + case fee + USB cable + power adapter + processing fee).</li>
        <li>Borrowers are responsible for damage to and/or loss or theft of loaned units.</li>
        <li>Do not leave the laptops unattended.</li>
        <li>In case of an emergency evacuation, take the laptop with you.</li>
        <li>Noncompliance with the above rules will result in a registration block and suspension of library privileges.</li>
    </ul>
    <br />
</div>
<h5 class="policySecTitle">Personal Data and Information</h5>
<div class="policyContent">
    <br />
    The laptop comes with a suite of preloaded applications. Borrowers may add additional data and applications, however all data from the laptop will be wiped and replaced with the standard set of preloaded apps once it is returned to the library.
    <br />
    Any additional apps or programs purchased by the borrower during the loan period must be done with the borrower’s own funds. Any data or documents saved to the laptop by the patron during the loan period will be permanently erased during the restore process.</div>
</p>
